#include<iostream>
using namespace std;

int main(){
	int num1, num2;
	float res;
	
	cout<<"Enter first number :";
	cin>>num1;
	
	cout<<"Enter second number :";
	cin>>num2;
	
	res = num1+num2;
	cout<<"\nAddition :"<<res<<endl;
	
	res = num1-num2;
	cout<<"Subtraction :"<<res<<endl;
	
	res = num1*num2;
	cout<<"Multiplication :"<<res<<endl;
	
	res = num1/num2;
	cout<<"Division :"<<res<<endl;
	
	
	
}
